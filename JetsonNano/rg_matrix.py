def start():
    GPIO.output(clk, GPIO.HIGH)
    GPIO.output(din, GPIO.HIGH)
    GPIO.output(din, GPIO.LOW)
    GPIO.output(clk, GPIO.LOW)
def stop():
    GPIO.output(clk, GPIO.HIGH)
    GPIO.output(din, GPIO.LOW)
    GPIO.output(din, GPIO.HIGH)
def write_byte(byte):
    GPIO.output(clk, GPIO.LOW)
    for i in range(8):
        if byte & 0x01 == 0x01:
            GPIO.output(din, GPIO.HIGH)
        else:
            GPIO.output(din, GPIO.LOW)
        GPIO.output(clk, GPIO.HIGH)
        GPIO.output(clk, GPIO.LOW)
